package Model;

public class Route {
    private int MaTuyenDuong;
    private String TenTuyenDuong;
    private String MoTa;

    public Route(){}

    public int getMaTuyenDuong() {
        return MaTuyenDuong;
    }

    public void setMaTuyenDuong(int maTuyenDuong) {
        MaTuyenDuong = maTuyenDuong;
    }

    public String getTenTuyenDuong() {
        return TenTuyenDuong;
    }

    public void setTenTuyenDuong(String tenTuyenDuong) {
        TenTuyenDuong = tenTuyenDuong;
    }

    public String getMoTa() {
        return MoTa;
    }

    public void setMoTa(String moTa) {
        MoTa = moTa;
    }
}
