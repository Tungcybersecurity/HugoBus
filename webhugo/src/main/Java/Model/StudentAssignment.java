package Model;

public class StudentAssignment {
    private int MaPhanCong;
    private  int MaHocSinh;
    private  int MaLichTrinhXe;
    private String NgayGiao;
    private  String DiaDiemDon;
    private String DiaDiemTra;
    private  String ThoiGianDon;

    public StudentAssignment(){}

    public int getMaPhanCong() {
        return MaPhanCong;
    }

    public void setMaPhanCong(int maPhanCong) {
        MaPhanCong = maPhanCong;
    }

    public int getMaHocSinh() {
        return MaHocSinh;
    }

    public void setMaHocSinh(int maHocSinh) {
        MaHocSinh = maHocSinh;
    }

    public int getMaLichTrinhXe() {
        return MaLichTrinhXe;
    }

    public void setMaLichTrinhXe(int maLichTrinhXe) {
        MaLichTrinhXe = maLichTrinhXe;
    }

    public String getNgayGiao() {
        return NgayGiao;
    }

    public void setNgayGiao(String ngayGiao) {
        NgayGiao = ngayGiao;
    }

    public String getDiaDiemDon() {
        return DiaDiemDon;
    }

    public void setDiaDiemDon(String diaDiemDon) {
        DiaDiemDon = diaDiemDon;
    }

    public String getDiaDiemTra() {
        return DiaDiemTra;
    }

    public void setDiaDiemTra(String diaDiemTra) {
        DiaDiemTra = diaDiemTra;
    }

    public String getThoiGianDon() {
        return ThoiGianDon;
    }

    public void setThoiGianDon(String thoiGianDon) {
        ThoiGianDon = thoiGianDon;
    }
}
