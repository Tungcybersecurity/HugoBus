package Model;

public class Vehicle {
    private int MaXe;
    private String BienSoXe;
    private short SoGhe;
    private  String LoaiXe;
    private int MaTaiXe;
    private  int MaBaoMau;

    public Vehicle(){}

    public int getMaXe() {
        return MaXe;
    }

    public void setMaXe(int maXe) {
        MaXe = maXe;
    }

    public String getBienSoXe() {
        return BienSoXe;
    }

    public void setBienSoXe(String bienSoXe) {
        BienSoXe = bienSoXe;
    }

    public short getSoGhe() {
        return SoGhe;
    }

    public void setSoGhe(short soGhe) {
        SoGhe = soGhe;
    }

    public String getLoaiXe() {
        return LoaiXe;
    }

    public void setLoaiXe(String loaiXe) {
        LoaiXe = loaiXe;
    }

    public int getMaTaiXe() {
        return MaTaiXe;
    }

    public void setMaTaiXe(int maTaiXe) {
        MaTaiXe = maTaiXe;
    }

    public int getMaBaoMau() {
        return MaBaoMau;
    }

    public void setMaBaoMau(int maBaoMau) {
        MaBaoMau = maBaoMau;
    }
}
